/*--- インクルードファイル ---*/
#include "StdAfx.h"
#include "MainProcess.h"

/*-----------------------------------------------------------------------------
/* メイン処理
-----------------------------------------------------------------------------*/
int main(void)
{
	//実行処理
	MainProcess main_process;

	bool is_success = main_process.StartUp();	//起動処理
	if(is_success)
	{
		main_process.Run();						//実行処理
	}
	main_process.ShutDown();					//停止処理

	return 0;
}

